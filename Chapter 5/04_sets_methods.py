# METHODS ON SETS
s = {32, 1, 2, 3, 23, 8, 12} 
print(len(s))                   # Returns 4, the length of the set  

s.remove(8)                     #Updates the set s and removes 8 from s. 
print(s)

s.pop()                         #Removes an arbitrary element from the set and return the element removed.
print(s)

s.clear()                       #empties the set s. 
print(s)
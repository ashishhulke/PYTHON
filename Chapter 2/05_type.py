
a = 31  
t = type(a) # class <int> 
print(t)

b = "31" 
t = type (b) # class <str> 
print(t)

c = 31.5  
t = type(c) # class <float>
print(t)

d = True 
t = type (d) # class <Boolean> 
print(t)

e = None
t = type (e) # class <NoneType> 
print(t)

s = "5.2"
f = float(s) # float function convert string s to float
t = type (f)
print(t)